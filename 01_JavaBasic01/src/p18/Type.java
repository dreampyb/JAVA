package p18;

public class Type {

	public static void main(String[] args) {
		int a; //정수형 변수
		a=13; //리터럴
		boolean b; //논리값(참,거짓) true, false
		b=true;
		b=false;
		String c ="123"; //"  " 문자열을 의미, 문자열은 String
		String d="이것도 문자";
		System.out.println(c);
		System.out.println(c + d);

	}

}
