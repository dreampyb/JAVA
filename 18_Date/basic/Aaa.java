package basic;

public class Aaa {
	private int a;
	private int b;
	
	public Aaa(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	@Override
	public String toString() {
		return "Aaa [a=" + a + ", b=" + b + "]";
	}



	
	
	
}
